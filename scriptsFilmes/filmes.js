function iniciaModal(modalID) {
    const modal = document.getElementById(modalID);
    modal.classList.add('mostrar');
    modal.addEventListener('click', (e) => {
        if(e.target.className == 'modal-close') {
            modal.classList.remove('mostrar');
        }
    })
}

let containerEl = document.querySelector('section');

function filmesNaPagina(filme) {
    let filmeDivEl = document.createElement('div');
    let filmeEl = document.createElement('img');
    let tituloEl = document.createElement('span');
    filmeDivEl.id = filme.id;
    filmeDivEl.classList.add('filmes');

    filmeEl.src = filme.img;
    filmeEl.classList.add('poster');

    filmeDivEl.classList.add('s');
    tituloEl.innerHTML = filme.titulo;
    tituloEl.classList.add('titulo-filme');

   filmeDivEl.appendChild(filmeEl);
   filmeDivEl.appendChild(tituloEl);
   containerEl.appendChild(filmeDivEl);
}


